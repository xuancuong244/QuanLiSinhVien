    public void Save() throws SQLException, ClassNotFoundException {
        txtIDStudent.requestFocus();
        Connection con = DataConnection.getConnection();
        for (CheckStudent sv : list) {
            if (txtIDStudent.getText().equals(sv.getIDStudent())) {
                JOptionPane.showMessageDialog(this, "ID Student alrealy available!");
                txtIDStudent.requestFocus();
                return;
            }
        }

        try {
            String sql = "insert into STUDENTS values(?,?,?,?,?,?,?)";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, txtIDStudent.getText());
            st.setString(2, txtFullname.getText());
            st.setString(3, txtEmail.getText());
            st.setString(4, txtPhone.getText());
            boolean gt;
            if (rdoMale.isSelected()) {
                gt = true;
            } else {
                gt = false;
            }
            st.setBoolean(5, gt);
            st.setString(6, txtAddress.getText());
            st.setString(7, path);
            st.executeUpdate();
            CheckStudent sv = new CheckStudent(txtIDStudent.getText(), txtFullname.getText(), txtEmail.getText(), txtPhone.getText(), txtAddress.getText(), gt, path);
            list.add(sv);
            JOptionPane.showMessageDialog(this, "Save successfully!");
            con.close();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Error");
        }
        New();
        showDetail(index);
        fillToTable();
    }

    public void Delete() throws SQLException, ClassNotFoundException {
        Connection con = DataConnection.getConnection();
        index = tblDanhSach.getSelectedRow();
        if (txtIDStudent.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Student to delete");
            txtIDStudent.requestFocus();
            return;
        }
        try {
            String sql = " delete from STUDENTS where MaSV =  ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setString(1, txtIDStudent.getText());
            st.execute();
            JOptionPane.showMessageDialog(this, "Delete successfully!");
            list.remove(index);

            st.close();
            con.close();

        } catch (Exception e) {
            System.out.println(e);

        }
//        New();
        showDetail(index = 0);
        fillToTable();
    }

    public void Update() throws SQLException, ClassNotFoundException {
        Connection con = DataConnection.getConnection();
        if (txtIDStudent.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Student to update");
            txtIDStudent.requestFocus();
            return;
        }
        try {
            String sql = "update STUDENTS set HoTen = ?, Email = ?, SDT = ?, gioitinh = ?, diachi = ?, hinh = ? where masv = ?";
            PreparedStatement st = con.prepareStatement(sql);

            st.setString(1, txtFullname.getText());
            st.setString(2, txtEmail.getText());
            st.setString(3, txtPhone.getText());
            boolean gt;
            if (rdoMale.isSelected()) {
                gt = true;
            } else {
                gt = false;
            }
            st.setBoolean(4, gt);
            st.setString(5, txtAddress.getText());

            st.setString(6, path);
            st.setString(7, txtIDStudent.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update successfully!");
            CheckStudent sv = new CheckStudent();
            sv.setIDStudent(txtIDStudent.getText());
            sv.setFullname(txtFullname.getText());
            sv.setEmail(txtEmail.getText());
            sv.setPhone(txtPhone.getText());
            sv.setGender(gt);
            sv.setAddress(txtAddress.getText());
            sv.setImage(path);
            list.set(index, sv);

            con.close();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Lỗi");
        }
        fillToTable();
        showDetail(index);
    }
