    public void fillToTable() {
        model.setRowCount(0);
        for (CheckStudent sv : list) {
            String gt = sv.isGender() ? "Male" : "Female";
            model.addRow(new Object[]{sv.getIDStudent(), sv.getFullname(), sv.getEmail(), sv.getPhone(), gt, sv.getAddress(), sv.getImage()});
        }
    }

